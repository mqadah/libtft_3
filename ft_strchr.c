#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	size_t	n;

	n = ft_strlen(s);
	i = 0;
	if ((char)c == '\0')
		return ((char *)(s + n));
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i++;
	}
	return (NULL);
}
